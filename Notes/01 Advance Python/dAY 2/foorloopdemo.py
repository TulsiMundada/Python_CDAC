#for loop demo
#if you know the number of iterations in advance
#to print numbers 1 to 10
for i in range(1,11): #1-10
    print(i,end=" ")
print()
print("end of loop")
for i in range(10): #0-9
    print(i,end=" ")
print()
print("end of loop")

for i in range(3,21,3): #0-9
    print(i,end=" ")
print()
print("end of loop")
